package com.kkb.pojo;

public class DrugPeople {
    private Integer dr_id;

    private Integer people_id;

    private Integer dr_number;

    private Integer dr_outNumber;

    public Integer getDr_id() {
        return dr_id;
    }

    public void setDr_id(Integer dr_id) {
        this.dr_id = dr_id;
    }

    public Integer getPeople_id() {
        return people_id;
    }

    public void setPeople_id(Integer people_id) {
        this.people_id = people_id;
    }

    public Integer getDr_number() {
        return dr_number;
    }

    public void setDr_number(Integer dr_number) {
        this.dr_number = dr_number;
    }

    public Integer getDr_outNumber() {
        return dr_outNumber;
    }

    public void setDr_outNumber(Integer dr_outNumber) {
        this.dr_outNumber = dr_outNumber;
    }
}